import mongoose from 'mongoose';

const requestedUserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Requested = mongoose.model('requested', requestedUserSchema);

export default Requested;
